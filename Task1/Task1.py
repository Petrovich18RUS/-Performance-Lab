import sys

# Функция для вычисления пути в круговом массиве
def find_circular_path(n, m):
    #массив чисел от 1 до n
    arr = list(range(1, n + 1))

    # начальные числа каждого интервала
    path = []
    # Начинаем с первого элемента
    current_index = 0

    # Бесконечный цикл - будем выходить из него когда вернемся к началу
    while True:
        # Берем число на текущей позиции
        current_element = arr[current_index]
        #начало интервала
        path.append(str(current_element))
        # Если вышли за границы - возвращаемся в начало
        current_index = (current_index + m - 1) % n
        # Проверяем, не вернулись ли мы к самому первому элементу
        # Если да - заканчиваем работу
        if current_index == 0:
            break
    # Объединяем все числа пути в одну строку и возвращаем
    return ''.join(path)

# Главная функция программы
def main():
    # Проверяем, что передано ровно 4 аргумента
    if len(sys.argv) != 5:
        print("Ошибка: необходимо передать 4 аргумента!")
        print("Использование: python program.py n1 m1 n2 m2")
        print("Пример: python program.py 6 3 5 4")
        print("Где:")
        print("  n1 - размер первого массива")
        print("  m1 - интервал для первого массива")
        print("  n2 - размер второго массива")
        print("  m2 - интервал для второго массива")
        sys.exit(1)  # Завершаем программу с кодом ошибки

    try:
        # sys.argv[1] - первый аргумент (n1)
        n1 = int(sys.argv[1])
        # sys.argv[2] - второй аргумент (m1)
        m1 = int(sys.argv[2])
        # sys.argv[3] - третий аргумент (n2)
        n2 = int(sys.argv[3])
        # sys.argv[4] - четвертый аргумент (m2)
        m2 = int(sys.argv[4])

        # Проверяем, что все числа положительные
        if n1 <= 0 or m1 <= 0 or n2 <= 0 or m2 <= 0:
            print("Ошибка: все параметры должны быть положительными числами!")
            print("Например: 1, 2, 3, ...")
            sys.exit(1)
        # Вычисляем путь для первого массива
        path1 = find_circular_path(n1, m1)
        # Вычисляем путь для второго массива
        path2 = find_circular_path(n2, m2)
        # Объединяем два пути в один
        result = path1 + path2

        # Выводим конечный результат
        print(result)

    except ValueError:
        # Эта ошибка возникает, если не получается преобразовать аргумент в число
        # Например, если передали "abc" вместо числа
        print("Ошибка: все аргументы должны быть целыми числами!")
        print("Проверьте, что передали числа, а не текст")
        sys.exit(1)
    except Exception as e:
        # Любая другая ошибка
        print(f"Произошла непредвиденная ошибка: {e}")
        sys.exit(1)


# Этот код выполняется только если файл запущен напрямую
if __name__ == "__main__":
    main()